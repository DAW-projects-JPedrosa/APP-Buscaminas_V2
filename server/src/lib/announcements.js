function seededRandom(seed) {
  let x = seed || Math.random()
  return function () {
      x = Math.sin(x) * 10000
      return x - Math.floor(x)
  }
}

function currentMinute() {
  return Math.floor(new Date().getTime() / 60000)
}

function currentAnnouncements() {
  const seed = currentMinute()
  const generator = seededRandom(seed)

  function randBetween(min, max) {
    return Math.floor(generator() * (max - min)) + min
  }

  const announcements = []
  const numberOfAnnouncements = randBetween(1, 3)

  do {
    const index = randBetween(0, ANNOUNCEMETS_LIST.length-1)
    const announcement = ANNOUNCEMETS_LIST[index]
    if(!announcements.includes(announcement)) announcements.push(announcement)
  } while (announcements.length < numberOfAnnouncements)

  return announcements
}

export default currentAnnouncements
